'''Dados cuatro números naturales A, B, C y D, escriba un algoritmo que dé la sumatoria'''

print("Ingrese el valor de 4 numeros naturales A, B, C, D")
a = int(input("Ingrese el valor de A: "))
b = int(input("Ingrese el valor de B: "))
c = int(input("Ingrese el valor de C: "))
d = int(input("Ingrese el valor de D: "))
sumatoria = a + b + c + d
print("La sumatoria de los 4 nuemeros son: ", sumatoria)