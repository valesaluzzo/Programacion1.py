'''Dados dos nombres escritos en minúscula obtengo la longitud de cada uno de ellos.
Detallar tipo de datos de la entrada y de la salida.'''


nombre1 = input("Ingresar nombre 1 en minusculas: ")
nombre2 = input("Ingresar nombre 2 en minusculas: ")
#La funcion len calcula la longitud de cada palabra
logitud1 = len(nombre1)
logitud2 = len(nombre2)
print("La longitud del primer nombre es de: ",logitud1)
print("La longitud del segundo nombre es de: ",logitud2)