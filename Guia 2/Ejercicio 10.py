'''La serie numérica de Fibonacci declara que cada término de la misma es la suma de los dos términos anteriores. 
Teniendo como datos el término N1 y el posterior N2, calcular y mostrar los 5 términos siguientes.'''

F0 = 0
F1 = 1
f2 = F0 + F1
f3 = F1 + f2
f4 = f2 + f3
f5 = f3 + f4
f6 = f4 + f5
print("Resultado: ",f2,f3,f4,f5,f6)